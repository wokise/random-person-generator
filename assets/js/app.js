    function capitalize_first_letter(str) {
        if (!str) return str;

        return str[0].toUpperCase() + str.slice(1);
    }

    function get_transliteration({ first, second, patronymic }) {
        first = toTranslit(first);
        second = toTranslit(second);
        patronymic = toTranslit(patronymic);

        return {
            first,
            second,
            patronymic,
        };
    }

    function get_login({ first, second }, { year: year_of_birthday }) {
        first = capitalize_first_letter(toTranslit(first));
        second = capitalize_first_letter(toTranslit(second));
        random_int = year_of_birthday.toString().slice(2, 4);

        return [first, second, random_int].join("");
    }

