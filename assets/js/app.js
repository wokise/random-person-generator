    function get_card() {
        let date = new Date();
        let curr_year = date.getFullYear();

        let bin = get_random_value(data_list.bins);
        let other = get_random_int(1000000000, 9999999999);

        let num = [...(bin + other)]
            .map((d, i) => (i % 4 == 0 ? " " + d : d))
            .join("")
            .trim();

        let month_end = get_random_int(1, 12);
        if (month_end < 10) month_end = "0" + month_end;
        let year_end = (curr_year + get_random_int(1, 5)).toString().slice(2, 4);

        let date_end = [month_end, year_end].join("/");
        let cvv = get_random_int(100, 999);

        return {
            num,
            date_end,
            cvv,
        };
    }

