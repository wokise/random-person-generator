    function get_password(len) {
        if (len > 10) len = 10;
        len = len * -1;

        return Math.random().toString(36).slice(len);
    }

    function get_photo({ gender }, age) {
        let photo = "";
        if (gender === "male") {
            if (age === "young-adult") photo = get_random_value(data_list.faces.male.young_adult);
            if (age === "adult") photo = get_random_value(data_list.faces.male.adult);
            if (age === "elderly") photo = get_random_value(data_list.faces.male.elderly);
        }
        if (gender === "female") {
            if (age === "young-adult") photo = get_random_value(data_list.faces.female.young_adult);
            if (age === "adult") photo = get_random_value(data_list.faces.female.adult);
            if (age === "elderly") photo = get_random_value(data_list.faces.female.elderly);
        }
        return photo;
    }

